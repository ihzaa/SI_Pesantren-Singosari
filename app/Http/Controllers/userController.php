    public function cek(Request $request)
    {
        $user = \App\user::where('username',$request->username)->first();
        if($user){
            if(Hash::check($request->password,$user->password)){
                if($user->role == 1){
                    return $user->admin;
                }else if($user->role == 2){
                    return $user->pengajar;
                }else{
                    return $user->santri;
                }
            }
        }
        return "salah";
